    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        // TODO add your handling code here:
        
        SL=new SetList();
        String getInput=jTextField1.getText().toUpperCase();
        
        
        
        if(!getInput.equals("")){
        
            DefaultListModel dlml= SL.CheckAndMakeList(getInput);
        
            int length=dlml.getSize();
        
       
            System.out.println("Size of dlml is : "+length);
        
            if(length==0)
        
            {
           
                JOptionPane.showMessageDialog(rootPane, "Sorry.. No Item Found !", "No Item !", JOptionPane.INFORMATION_MESSAGE);
        
                jTextField1.setText("");
            }
        
            else{
           
                jList1.setModel(dlml);
        
                JOptionPane.showMessageDialog(rootPane, length+" Items Found .", "Found Item !", JOptionPane.INFORMATION_MESSAGE);
            
                jTextField1.setText("");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane, "Sorry.. You've to insert at least one letter ..!", "Error input !", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jTextField1ActionPerformed

