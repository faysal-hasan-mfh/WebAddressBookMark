    private void INSERTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_INSERTActionPerformed
        // TODO add your handling code here:
        
        if(isDatabaseCreated==false)
        {
            int getIntValue= JOptionPane.showConfirmDialog(rootPane, "Database is not created ! Do you"
                + " want to create it? ");
         
            System.out.println(getIntValue);
         
        
            if(getIntValue==0){
            
                createDatabase.CreateDatabase objCD=new CreateDatabase();
                
                objCD.createDatabaseMethod();
                DatabaseExistence dbe=new DatabaseExistence();
                
                if(dbe.dbExist()) {
                    isDatabaseCreated=true;
                    
                    ResetButton.setEnabled(isDatabaseCreated);
                    String createdDB="Database Created.. Press Insert to add Item.";
                    
                     dfm.addElement(createdDB);
            
                     jList1.setModel(dfm);
                    
                }
                else{
                isDatabaseCreated=false;
                }
            
            }
       
       
        }
        
        else
        {
        
           int getYesValue=JOptionPane.showConfirmDialog(rootPane, "Do you want to insert new website address ? ", "Insert confirmation ?", JOptionPane.YES_NO_OPTION);
        
            if(getYesValue==0)
            {
            
            InsertIntoDatabase obj_CD = new InsertIntoDatabase();
            
            obj_CD.setVisible(true);
            obj_CD.setLocationRelativeTo(null);
            }
            
        
        }
        
    }//GEN-LAST:event_INSERTActionPerformed

